    private void ponerDatosEnLosLbl() {
//        System.out.println(this.objVaca.getNumero());
        this.lblNumeroVaca.setText(Integer.toString(this.objVaca.getNumero()));

        //Modificaciones
        if (this.objVaca.getFechaNacimiento() == null) {
            this.lblFechaNa.setText("Sin fecha");
        } else {
            this.lblFechaNa.setText(this.objVaca.getFechaNacimiento().toString());

        }

        this.lblEstado.setText(this.objVaca.getEstado());
        this.lblGenero.setText(this.objVaca.getGenero());
        this.lblKilos.setText(Float.toString(this.objVaca.getKilos()));
        this.lblTipoVaca.setText(this.objVaca.getTipoVaca());
        this.lblPotrero.setText(this.objVaca.getPotrero());
        this.txtAreaDescripcion.setText(this.objVaca.getDescripcion());

        Propietario objPropietario = new Propietario();
        objPropietario.setCedula(this.objVaca.getCedula());

        PropietarioDB objPropietarioDB = new PropietarioDB();
        objPropietario = (Propietario) objPropietarioDB.listarUno(objPropietario);

        this.lblPropietario.setText(objPropietario.getNombres());
    }

